    private void GenerarReportePDF() {
        try {
        // Obtener el apellido del alumno para el nombre del archivo
            String apellidoAlumno = tabla.getValueAt(0, 6).toString();
            Document document = new Document();
            String userHome = System.getProperty("user.home");
            String documentosPath = userHome + File.separator + "Documentos";
            String reportePath = documentosPath + File.separator + "Reporte";
            File reporteDir = new File(reportePath);
            if (!reporteDir.exists()) {
                reporteDir.mkdirs();
            }
            String filePath = reportePath + File.separator + "ReporteAsistencia" + apellidoAlumno+  ".pdf";
            
            PdfWriter.getInstance(document, new FileOutputStream(filePath));
            document.open();
            // Crear una tabla para el logo y el título
            PdfPTable headerTable = new PdfPTable(2);
            headerTable.setWidthPercentage(100);
            headerTable.setWidths(new float[] {1, 4});
            Paragraph titulo = new Paragraph("CONTROL DE Pensiones", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18));
            titulo.setAlignment(Element.ALIGN_CENTER);
            document.add(titulo);
            document.add(Chunk.NEWLINE);
        
        //Logo
            String logoPath = "C:\\Users\\Smit1\\OneDrive\\Documentos\\Proyectos\\JAVA\\Proyecto Amigos School -Version 3.1\\src\\Imagenes\\0.png"; // Cambia esta ruta por la ruta de tu logo
            Image logo = Image.getInstance(logoPath);
            logo.scaleToFit(100, 100);
            logo.setAbsolutePosition(40, document.getPageSize().getRight() +140);
            document.add(logo);
            document.add(Chunk.NEWLINE);
            Paragraph title = new Paragraph("I.E AMIGOS SCHOOL", FontFactory.getFont(FontFactory.TIMES_ITALIC, 12));
            title.setAlignment(Element.ANNOTATION);
            document.add(title);
            document.add(Chunk.NEWLINE);
        
        // Información del grado, sección y tutor
            String fechaActual = new SimpleDateFormat("dd/MM/yyyy").format(new Date());
            PdfPTable infoTable = new PdfPTable(4);
            infoTable.setWidthPercentage(100);
            infoTable.addCell(getColoredCell("Codigo: ", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY)); // Celda vacía
            infoTable.addCell(getColoredCell(tabla.getValueAt(0, 0).toString(), PdfPCell.ALIGN_CENTER, BaseColor.WHITE)); // Celda vacía
            infoTable.addCell(getColoredCell("Fecha:", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            infoTable.addCell(getColoredCell(fechaActual, PdfPCell.ALIGN_CENTER, BaseColor.WHITE)); // Ejemplo de fecha de exportación
            infoTable.addCell(getColoredCell("Tutor: ", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            infoTable.addCell(getColoredCell(tabla.getValueAt(0, 3).toString() + " " +tabla.getValueAt(0, 4).toString(), PdfPCell.ALIGN_CENTER, BaseColor.WHITE));// Tutor
            infoTable.addCell(getColoredCell("Grado Académico:", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY)); // Grado
            infoTable.addCell(getColoredCell(tabla.getValueAt(0, 1).toString(), PdfPCell.ALIGN_CENTER, BaseColor.WHITE)); // Grado
            infoTable.addCell(getColoredCell("Sección:", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY)); // Sección
            infoTable.addCell(getColoredCell(tabla.getValueAt(0, 2).toString() , PdfPCell.ALIGN_CENTER, BaseColor.WHITE)); // Sección
            infoTable.addCell(getColoredCell("Colegio:", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY)); // Celda vacía
            infoTable.addCell(getColoredCell("Amigos School", PdfPCell.ALIGN_CENTER, BaseColor.WHITE)); // Celda vacía

            document.add(infoTable);
            document.add(Chunk.NEWLINE);

        // Tabla de datos del alumno
            DefaultTableModel model = (DefaultTableModel) tabla.getModel();
            int numRows = model.getRowCount();
            PdfPTable alumnoTable = new PdfPTable(6);
            alumnoTable.setWidthPercentage(100);
            alumnoTable.addCell(getColoredCell("Apellido", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            alumnoTable.addCell(getColoredCell("Nombre", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            alumnoTable.addCell(getColoredCell("Fecha", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            alumnoTable.addCell(getColoredCell("Monto", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            alumnoTable.addCell(getColoredCell("Cuota", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
            alumnoTable.addCell(getColoredCell("Condicion", PdfPCell.ALIGN_CENTER, BaseColor.LIGHT_GRAY));
        
        
            for (int i = 0; i < numRows; i++) {
                alumnoTable.addCell(model.getValueAt(i, 5).toString());
                alumnoTable.addCell(model.getValueAt(i, 6).toString());
                alumnoTable.addCell(model.getValueAt(i, 7).toString());
                alumnoTable.addCell(model.getValueAt(i, 8).toString());
                alumnoTable.addCell(model.getValueAt(i, 9).toString());
                alumnoTable.addCell(model.getValueAt(i, 10).toString());
            }
            document.add(alumnoTable);
            document.add(Chunk.NEWLINE);

            document.close();
            if (Desktop.isDesktopSupported()) {
                Desktop desktop = Desktop.getDesktop();
                File pdfFile = new File(filePath);
                if (pdfFile.exists()) {
                    desktop.open(pdfFile);
                }
            }
            System.out.println("PDF generado exitosamente!");
            JOptionPane.showMessageDialog(null, "PDF generado exitosamente!");

        } catch (DocumentException | HeadlessException | FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Error al generar PDF: " + e.getMessage());
        } catch (IOException ex) {
            Logger.getLogger(ReportePensiones.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private static PdfPCell getColoredCell(String text, int alignment, BaseColor bgColor) {
        PdfPCell cell = new PdfPCell(new Phrase(text));
        cell.setPadding(5);
        cell.setHorizontalAlignment(alignment);
        cell.setVerticalAlignment(Element.ALIGN_MIDDLE);
        cell.setBackgroundColor(bgColor);
        cell.setBorderColor(BaseColor.BLACK);
        return cell;
    }

